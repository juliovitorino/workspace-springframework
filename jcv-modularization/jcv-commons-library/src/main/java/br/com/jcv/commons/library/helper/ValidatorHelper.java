package br.com.jcv.commons.library.helper;

import br.com.jcv.commons.library.commodities.exception.InvalidFormatException;
import org.springframework.http.HttpStatus;

import java.util.regex.Matcher;
import java.util.regex.Pattern;

public class ValidatorHelper {

  public static void execute(String valueParse, String regex, String correctForm) {
    Pattern er = Pattern.compile(regex);
    Matcher result = er.matcher(valueParse);
    if (!result.matches()) {
      throw new InvalidFormatException("SGN-0000:O conteúdo " + valueParse + " não está no formato correto. O formato correto deve ser " + correctForm,HttpStatus.BAD_REQUEST);
    }
  }
}














































































































































































